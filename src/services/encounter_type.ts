export function createEncounterType(patientId: number, typeId: EncounterTypeId, providerId: number): AppEncounterService {
    return new AppEncounterService(patientId, typeId, providerId);
}

export async function saveEncounterData(patientId: number, typeId: EncounterTypeId, providerId: number, data: any) {
    if (data.length > 0) {
        const encounter = createEncounterType(patientId, typeId, providerId);
        await encounter.createEncounter();
        await encounter.saveObservationList(data);
    }
}
